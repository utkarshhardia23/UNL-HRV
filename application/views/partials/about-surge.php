<p> Growing world-wide instability caused by both governmental/state and non-state actors makes social unrest a great concern to researchers and policy makers.  
In recent years, big data ap-proaches related to widespread use of internet-based and social media have revolutionized our ability to harness, understand, and even anticipate
 the occurrence and spread of social unrest.  Social scientific definitions of unrest typically revolve around sustained series of events that in-volve collective action a
 nd are aimed at producing some type of systemic change.  Such events can be initiated by both state and non-state actors and can be aimed at a government or at other social 
 institutions.   The repertoire of such protest events, which have a tendency to occur in “cy-cles of contention,” includes non-violent demonstrations, strikes, 
 riots, civil wars (including gen-ocides), coups, revolts and revolutions/regime changes.  Of particular interest to national security experts is the fact that these 
 cycles of contention are speeding up and becoming ever more vola-tile, precisely because of technological changes in how information is transmitted and shared.    </p>
 <p>
In order to address the need to anticipate these unrest events, our goal is to create a system to detect and ultimately anticipate social unrest events in India/Pakistan/Bangladesh 
using an inte-grated approach which combines Computer Science (CS) data-driven techniques and Social Sci-ence model-driven methodologies.  As a result, this research will further 
scientific approaches to improving situational awareness by identifying short-term triggers that spark social unrest, and factors that fuel long-term social unrest events. </p>

<p>
This research has five vital components: 1) develop social unrest vocabulary for our region of interest to perform content analysis of classic media; 2) apply a dynamic version of this 
vocabu-lary to social media data, 3) develop spatio-temporal algorithms to mine the data collected from both traditional and social media sources; 4) develop a social science theory 
based explanatory model to identify long term triggers of social unrest; 5) develop a multiagent simulation testbed for entity-entity interactions across time and space to analyze and 
predict short-term social unrest triggers modeling and simulation. All of these components will then be integrated into a social unrest anticipatory system with a map based front end 
called SURGE – Social Unrest Recon-naissance GazEtteer.
</p>

<p>
Currently the primary source for event data is the Global Database of Events, Language, and Tone (GDELT) - http://gdeltproject.org/. 
GDELT offers a platform that monitors the news media from all over the world in print, broadcast, and web formats, in over 100 languages, 
every mo-ment of every day. It stretches back to January 1, 1979 through present day, with daily up-dates. We are working on adding Times of India’s newspaper archives and 
Twitter feeds to our dataset.
</p>
<p>
The expected benefits from SURGE are to find answers for the following questions - What are the different types of social unrests? Where and when do they occur? 
How do unrest events evolve/diffuse over space and time? What was the situation before and after a key event? What are long-term trends vs. short-term trends? 
What are the main hotspots? What is happening in a specific region of interest (ROI)?
